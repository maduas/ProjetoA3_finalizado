    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel)tabelaProdutos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (Produto p : pdao.read()) {
            modelo.addRow(new Object[]{
            p.getID(),
            p.getNome(),
            p.getValor(),
            p.getSabor(),
            p.getTamanho(),
            p.getDescricao(),
            p.getQtde_estoque(),
            p.getDisponivel()
            });
        }
    }
    
