    private void excluirBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirBotaoActionPerformed
        // TODO add your handling code here:
        if (tabelaProdutos.getSelectedRow() != -1){
            Produto p = new Produto();
            ProdutoDAO dao = new ProdutoDAO();
            p.setID((int)tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0));
            dao.delete(p);
            stringNome.setText("");
            doubleValor.setText("");
            stringSabor.setText("");
            stringTamanho.setText("");
            stringDescricao.setText("");
            intQtde.setText("");
            stringDisponivel.setText("");
        
        readJTable();
        }else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir");
        }
    }//GEN-LAST:event_excluirBotaoActionPerformed

