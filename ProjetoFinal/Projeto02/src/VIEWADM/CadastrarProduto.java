    private void botaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarActionPerformed
        // TODO add your handling code here:
        if (tabelaProdutos.getSelectedRow() != -1) {
            Produto p = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        p.setNome(stringNome.getText());
        p.setValor(Double.parseDouble(doubleValor.getText()));
        p.setSabor(stringSabor.getText());
        p.setTamanho(stringTamanho.getText());
        p.setDescricao(stringDescricao.getText());
        p.setQtde_estoque(Integer.parseInt(intQtde.getText()));
        p.setDisponivel(stringDisponivel.getText());
        p.setID((int)tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0));
        
        dao.update(p);
        
        stringNome.setText("");
        doubleValor.setText("");
        stringSabor.setText("");
        stringTamanho.setText("");
        stringDescricao.setText("");
        intQtde.setText("");
        stringDisponivel.setText(""); 
        
        readJTable();
        }
    }//GEN-LAST:event_botaoAlterarActionPerformed

