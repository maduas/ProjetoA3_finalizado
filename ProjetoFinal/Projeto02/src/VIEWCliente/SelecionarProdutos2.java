    private void adcarrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adcarrinhoActionPerformed
        // TODO add your handling code here:
        
        if (tabelaProdutoss.getSelectedRowCount()!=0){ //Verifica se existe linha selecionada para não dar erro na hora de pegar os valores
        Produto p= new Produto();
        p.setQtde(Integer.parseInt((String)jTextField1.getText()));
   
        
	//Pega os models das listas, para fazer as inserções e remoções
	DefaultTableModel modelOrigem = (DefaultTableModel) tabelaProdutoss.getModel();
	DefaultTableModel modelDestino = (DefaultTableModel) tablecarrinho.getModel();
        
        
	//Cria uma linha para ser incluida na tabela de destino, no meu caso tem duas colunas, adapte para as suas tabelas
	Object[] obj = {
            tabelaProdutoss.getValueAt(tabelaProdutoss.getSelectedRow(), 1),
            tabelaProdutoss.getValueAt(tabelaProdutoss.getSelectedRow(), 2),
            tabelaProdutoss.getValueAt(tabelaProdutoss.getSelectedRow(), 3),
            p.getQtde(),
        };

	//Adiciona no destino e remove da origem
	modelDestino.addRow(obj);
        } else { 
            JOptionPane.showMessageDialog(null, "Nenhum produto selecionado");

    }//GEN-LAST:event_adcarrinhoActionPerformed
    }
    
