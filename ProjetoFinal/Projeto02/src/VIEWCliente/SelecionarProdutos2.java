    public void readJTable2(){
        DefaultTableModel modelo = (DefaultTableModel)tabelaProdutoss.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for(Produto p : pdao.read()) {
            modelo.addRow(new Object[]{
            p.getID(),
            p.getNome(),
            p.getValor(),
            p.getSabor(),
            p.getTamanho(),
            p.getDescricao(),
            p.getDisponivel()
            });
        }
    }
    public void readJTableForNome(String NOME){
        DefaultTableModel modelo = (DefaultTableModel)tabelaProdutoss.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (Produto p : pdao.readForDesc(NOME)) {
            modelo.addRow(new Object[]{
            p.getID(),
            p.getNome(),
            p.getValor(),
            p.getSabor(),
            p.getTamanho(),
            p.getDescricao(),
            p.getDisponivel()
            });
        }
    }
   
    
  

